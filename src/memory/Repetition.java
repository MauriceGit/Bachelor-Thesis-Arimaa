package memory;

public class Repetition {

	public long hash = 0L;
	public int counter = 0;

	/**
	 * Konstruktor und Setzen von Attributen eines Spielfelds.
	 * 
	 * @param hash
	 * @param counter
	 */
	public Repetition(long hash, int counter) {
		this.hash = hash;
		this.counter = counter;
	}

}
